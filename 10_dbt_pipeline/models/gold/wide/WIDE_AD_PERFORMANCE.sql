left join {{ ref('DIM_CAMPAIGN') }}    c  on f.CAMPAIGN_SK    = c.CAMPAIGN_SK
left join {{ ref('DIM_AD_CREATIVE') }} ac on f.AD_CREATIVE_SK = ac.AD_CREATIVE_SK
left join {{ ref('DIM_DEVICE') }}      dv on f.DEVICE_SK      = dv.DEVICE_SK
