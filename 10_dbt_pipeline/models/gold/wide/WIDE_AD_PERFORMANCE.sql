select
    f.PERF_DATE_SK,
    f.AD_COST, f.IMPRESSIONS, f.CLICKS, f.INBOUND_CALL,
    f.GA_CONV_MEMBERS, f.GA_CONV_CNT,
    f.DAY_OF_WEEK, f.WEEK_OF_YEAR, f.TIME_BAND,
    f.CM_POSITION, f.RT_TYPE, f.AD_START_TIME, f.BROADCAST_DATE,
