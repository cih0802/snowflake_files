SELECT
  NULLIF(TRIM(SPNSR_NO),'')            AS SPNSR_NO,
  SPNSR_BSNS_NO                        AS SPNSR_BSNS_NO,
  NULLIF(TRIM(SPNSR_BSNS_ID),'')       AS SPNSR_BSNS_ID,
  SPNSR_AMT                            AS SPNSR_AMT,
  NULLIF(TRIM(SPNSR_DSCNTC_YN),'')     AS SPNSR_DSCNTC_YN,
  NULLIF(TRIM(SPNSR_DSCNTC_DE),'')     AS SPNSR_DSCNTC_DE,
  NULLIF(TRIM(SPNSR_DSCNTC_RSN_CD),'') AS SPNSR_DSCNTC_RSN_CD,
  'CRM'                                AS DW_SOURCE_SYSTEM,
  CURRENT_TIMESTAMP()                  AS DW_LOAD_TS,
  CURRENT_TIMESTAMP()                  AS DW_UPDATE_TS,
  NULL                                 AS DW_BATCH_ID
FROM GN_DW.BRONZE_CRM.TM_MM_FDRM_MBER_SPNSR_BSNS
WHERE SPNSR_NO IS NOT NULL AND SPNSR_BSNS_NO IS NOT NULL
QUALIFY ROW_NUMBER() OVER (PARTITION BY SPNSR_NO, SPNSR_BSNS_NO ORDER BY SPNSR_DSCNTC_DE DESC NULLS LAST) = 1