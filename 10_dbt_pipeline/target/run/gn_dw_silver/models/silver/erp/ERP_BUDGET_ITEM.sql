
  
    

        create or replace transient table GN_DW.SILVER.ERP_BUDGET_ITEM
         as
        (-- ERP_BUDGET_ITEM: 예산과목 마스터 (MD5 복합키, TOTAL 요약행 제외), 정본 09 STEP4.
-- Co-authored with CoCo
SELECT DISTINCT
  MD5(COALESCE(TO_VARCHAR(YEAR),'')||'|'||COALESCE(INCOME_EXPS_DIV_NM,'')||'|'||COALESCE(BDGT_UNIT_NM,'')||'|'||
      COALESCE(JANG_NM,'')||'|'||COALESCE(KWAN_NM,'')||'|'||COALESCE(HANG_NM,'')||'|'||
      COALESCE(MOK_NM,'')||'|'||COALESCE(DTL_ITEM_NM,'')||'|'||COALESCE(SUBDTL_ITEM_NM,'')||'|'||COALESCE(FUND_SOURCE_NM,'')) AS BUDGET_ITEM_DK,
  TRY_TO_NUMBER(YEAR)              AS BUDGET_YEAR,
  NULLIF(TRIM(INCOME_EXPS_DIV_NM),'') AS INCOME_EXPENSE_DIV,
  NULLIF(TRIM(BDGT_UNIT_NM),'')    AS BUDGET_UNIT_NM,
  NULLIF(TRIM(JANG_NM),'')         AS JANG_NM,
  NULLIF(TRIM(KWAN_NM),'')         AS KWAN_NM,
  NULLIF(TRIM(HANG_NM),'')         AS HANG_NM,
  NULLIF(TRIM(MOK_NM),'')          AS MOK_NM,
  NULLIF(TRIM(DTL_ITEM_NM),'')     AS DTL_ITEM_NM,
  NULLIF(TRIM(SUBDTL_ITEM_NM),'')  AS SUBDTL_ITEM_NM,
  NULLIF(TRIM(FUND_SOURCE_NM),'')  AS FUND_SOURCE_NM,
  'ERP'                            AS DW_SOURCE_SYSTEM,
  'BRONZE_ERP.BDGT_ACMSLT_LEDGER'  AS DW_SOURCE_TABLE,
  CURRENT_TIMESTAMP()              AS DW_LOAD_TS,
  CURRENT_TIMESTAMP()              AS DW_UPDATE_TS,
  NULL                             AS DW_BATCH_ID
FROM GN_DW.BRONZE_ERP.BDGT_ACMSLT_LEDGER
WHERE INCOME_EXPS_DIV_NM <> 'TOTAL'
        );
      
  